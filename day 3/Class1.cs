using System;

namespace mycalculator
{
    public class mycalculatorclass
    {
        public int add(int x, int y)
        {
            return x + y;
        }
        public int product(int x, int y)
        {
            return x * y;
        }
        public int cube(int x)
        {
            return x * x*x;
        }
    }
}
